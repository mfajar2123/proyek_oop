    private void update_imageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_imageActionPerformed
        // TODO add your handling code here:
        String ID = id.getText();
        String NAMA_IMAGE = lblnmfile.getText();
        String JUDUL = (String) txttitle.getSelectedItem();
        if (ID.equals("") || NAMA_IMAGE.equals("") || JUDUL.equals("Pilih")){
            JOptionPane.showMessageDialog(null, "Harap isi pilih file dan jenis file dengan benar!");
        }else {
            try {
                String path=new File(".").getCanonicalPath();
                FileUtils.copyFileToDirectory(file, new File(path+"/image"));

                java.sql.Connection Vconn = (Connection)koneksiDB.configDB();
                java.sql.PreparedStatement pst = Vconn.prepareStatement("UPDATE gambar SET image = '"+NAMA_IMAGE+"', title = '"+JUDUL+"' WHERE ID = '"+ID+"' AND title = '"+JUDUL+"' ");
                pst.execute();
                JOptionPane.showMessageDialog(null, "File Berhasil diupdate");
                reset_image();
            } catch (IOException ex) {
                Logger.getLogger(SaveImage.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex ) {
                Logger.getLogger(mn_survey.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_update_imageActionPerformed

