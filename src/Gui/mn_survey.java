    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        // TODO add your handling code here:
        String ID = id.getText();
        String NAMA = nama.getText();
        String NIK = nik.getText();
        String NO_TELP = no_telp.getText();
        String PENGHASILAN = penghasilan.getText();
        String PINJAMAN = pinjaman.getText();
        String PERIODE = periode.getText();
        String ANGSURAN = angsuran.getText();
        String RUMAH = foto_rumah.getText();
        String SP = surat_pernyataan.getText();
        String STATUS="Survey";

        if(NAMA.equals("")||NIK.equals("")||PINJAMAN.equals("") ||PERIODE.equals("")||NO_TELP.equals("")||PENGHASILAN.equals("")||RUMAH.equals("")||SP.equals("")){
            JOptionPane.showMessageDialog(null,"Harap isi formulir dengan benar dan lengkap !");
        }else{
            try {
                java.sql.Connection Vconn = (Connection)koneksiDB.configDB();
                java.sql.PreparedStatement pst = Vconn.prepareStatement("UPDATE tb_pengajuan SET nama = '"+NAMA+"', nik = '"+NIK+"', no_telp = '"+NO_TELP+"', penghasilan = '"+PENGHASILAN+"', pinjaman = '"+PINJAMAN+"', periode = '"+PERIODE+"', angsuran = '"+ANGSURAN+"', foto_rumah = '"+RUMAH+"', surat_pernyataan = '"+SP+"',  status = '"+STATUS+"' WHERE ID = '"+ID+"'");
                pst.execute();
                
                String JUDUL1="Foto Rumah";
                String JUDUL2="Surat Pernyataan";
//              String GetId = r.getString("ID");
                java.sql.Connection image = (Connection)koneksiDB.configDB();
                java.sql.PreparedStatement simpan_gambar = image.prepareStatement("INSERT INTO gambar (ID,title,image) VALUES ('"+ID+"','"+JUDUL1+"','"+RUMAH+"')");
                simpan_gambar.execute();
                
                java.sql.Connection image1 = (Connection)koneksiDB.configDB();
                java.sql.PreparedStatement simpan_gambar1 = image1.prepareStatement("INSERT INTO gambar (ID,title,image) VALUES ('"+ID+"','"+JUDUL2+"','"+SP+"')");
                simpan_gambar1.execute();
                
                JOptionPane.showMessageDialog(null, "Berkas berhasil dilengkapi");
                display_data(); //refresh table
                reset();
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(null, "Berkas gagal dilengkapi");
            }
        }
    }//GEN-LAST:event_ubahActionPerformed

