    private void display_data()
    {
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("ID");
        tabel.addColumn("Nama");
        tabel.addColumn("NIK");
        tabel.addColumn("No. Telp");
        tabel.addColumn("Penghasilan");
        tabel.addColumn("Pinjaman");
        tabel.addColumn("Periode");
        tabel.addColumn("Angsuran");
        tabel.addColumn("Rumah");
        tabel.addColumn("SP");
        String STATUS = "Survey";
        
        try{
            java.sql.Connection Vconn = (Connection)koneksiDB.configDB();
            java.sql.Statement s = Vconn.createStatement();
            java.sql.ResultSet r = s.executeQuery("SELECT * FROM tb_pengajuan WHERE status = '"+STATUS+"' ");
//            SELECT id_user FROM tb_user ORDER BY id_user DESC LIMIT 1
            
            //menampilkan data
            while (r.next()){
                tabel.addRow(new Object[]{
                   r.getString(1),r.getString(2),r.getString(3), r.getString(4), r.getString(5),r.getString(6),r.getString(7), r.getString(8),r.getString(13), r.getString(14)
                }); 
            }
            jTable1.setModel(tabel);
            
            
        }catch (SQLException e){
            
        }
    }

